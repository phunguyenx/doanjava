    private void jXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jXoaActionPerformed
        // TODO add your handling code here:
        int t = jTable1.getSelectedRow();
        if (t == -1) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn khách hàng cần xóa");
        } else {
            int cf = JOptionPane.showConfirmDialog(null,
                    "Bạn có muốn xóa khách hàng này không?", "Xác Nhận", JOptionPane.YES_NO_OPTION);
            if (cf == 0) {
                GuestControl.Remove(jTable1.getValueAt(t, 0).toString());
                model.removeRow(t);
                jTable1.updateUI();
            }

        }
    }//GEN-LAST:event_jXoaActionPerformed

    private void jsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jsuaActionPerformed
        // TODO add your handling code here:
        String id = jID.getText();
        String name = jName.getText();
        String idCard = jIdCard.getText();
        int gender = -1;
        if (jNam.isSelected()) {
            gender = 1;
        } else if (jNu.isSelected()) {
            gender = 0;
        }
        Date Bdate = jBirthDay.getDate();
        String phone = jPhone.getText();
        try {
            long x = Integer.parseInt(idCard.substring(0, 7));
            long z = Integer.parseInt(idCard.substring(7, idCard.length()));
            long y = Integer.parseInt(phone.substring(0, 7));
            long y1 = Integer.parseInt(phone.substring(7, phone.length()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "CMND hoặc số điện thoại không hợp lệ");
            return;
        }
        int i = jTable1.getSelectedRow();
        if (i == -1) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn khách hàng cần sửa");
        } else {
            int check = GuestControl.edit(id, name, idCard, gender, Bdate, phone);
            switch (check) {
                case 1 -> {
                    JOptionPane.showMessageDialog(null, "Sửa thông tin thành công");
                    jTable1.setValueAt(name, i, 1);
                    jTable1.setValueAt(idCard, i, 3);
                    if (jNam.isSelected()) {
                        jTable1.setValueAt("Nam", i, 2);
                    } else if (jNu.isSelected()) {
                        jTable1.setValueAt("Nữ", i, 2);
                    }
                    java.sql.Date sqlDate = new java.sql.Date(Bdate.getTime());
                    jTable1.setValueAt(sqlDate, i, 5);
                    jTable1.setValueAt(phone, i, 4);
                    jTable1.updateUI();
                }
                case -1 ->
                    JOptionPane.showMessageDialog(null, "Khách hàng không đủ tuổi");

                case 0 ->
                    JOptionPane.showMessageDialog(null, "Đã xảy ra lỗi, vui lòng thử lại");
                default ->
                    throw new AssertionError();
            }
        }
    }//GEN-LAST:event_jsuaActionPerformed

    private void jThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jThemActionPerformed
        // TODO add your handling code here:
        String id = jID.getText();
        String name = jName.getText();
        String idCard = jIdCard.getText();
        int gender = -1;
        if (jNam.isSelected()) {
            gender = 1;
        } else if (jNu.isSelected()) {
            gender = 0;
        }
        Date Bdate = jBirthDay.getDate();
        String phone = jPhone.getText();
        int check = GuestControl.check(id, name, idCard, gender, Bdate, phone);
        switch (check) {
            case -1 ->
                JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin");
            case -2 ->
                JOptionPane.showMessageDialog(null, "Bấm làm mới trước khi thêm nhân viên mới");
            case -3 ->
                JOptionPane.showMessageDialog(null, "Số chứng minh nhân dân bị trùng lặp");
            case -4 ->
                JOptionPane.showMessageDialog(null, "CMND hoặc số điện thoại không hợp lệ");
            case -5 ->
                JOptionPane.showMessageDialog(null, "Khách Hàng Không Đủ Tuổi");
            case 1 -> {
                JOptionPane.showMessageDialog(null, "Thêm thành công!");
                Guest e = new Guest();
                e.setId(id);
                e.setName(name);
                e.setIdCard(idCard);
                e.setGender(gender);
                e.setPhone(phone);
                e.setBirthday(Bdate);

                listGuests.add(e);
                showResult();
                jID.setText(GuestControl.ReturnNextID());
            }
            default ->
                throw new AssertionError();
        }
    }//GEN-LAST:event_jThemActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int vitri = jTable1.getSelectedRow();
        jID.setText(jTable1.getValueAt(vitri, 0).toString());
        jName.setText(jTable1.getValueAt(vitri, 1).toString());
        jIdCard.setText(jTable1.getValueAt(vitri, 3).toString());
        if (jTable1.getValueAt(vitri, 2).equals("Nam")) {
            jNam.setSelected(true);
        } else {
            jNu.setSelected(true);
        }
        try {
            jBirthDay.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(jTable1.getValueAt(vitri, 5).toString()));
        } catch (ParseException ex) {
            Logger.getLogger(FormNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
        jPhone.setText(jTable1.getValueAt(vitri, 4).toString());
    }//GEN-LAST:event_jTable1MouseClicked

