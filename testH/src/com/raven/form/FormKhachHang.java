    public final void showTable() {
        for (Guest e : listGuests) {
            if (e.getGender() == 1) {
                model.addRow(new Object[]{
                    e.getId(), e.getName(), "Nam", e.getIdCard(), e.getPhone(), e.getBirthday()
                });

            } else {
                model.addRow(new Object[]{
                    e.getId(), e.getName(), "Nữ", e.getIdCard(), e.getPhone(), e.getBirthday()
                });
            }
        }
    }

    public void showResult() {
        Guest e = listGuests.get(listGuests.size() - 1);
        Date date = e.getBirthday();
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        if (e.getGender() == 1) {
            model.addRow(new Object[]{
                e.getId(), e.getName(), "Nam", e.getIdCard(), e.getPhone(), dateFormat.format(e.getBirthday())
            });

        } else {
            model.addRow(new Object[]{
                e.getId(), e.getName(), "Nữ", e.getIdCard(), e.getPhone(), dateFormat.format(e.getBirthday())
            });
        }
    }

