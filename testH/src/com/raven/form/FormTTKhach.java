    public FormTTKhach(String username) {
        initComponents();
        setLocationRelativeTo(null);
        setResizable(false);
        ButtonGroup group = new ButtonGroup();
        group.add(jNam);
        group.add(jNu);
        Guest g = Controller.GuestControl.findByUsername(username);
        
        name.setText(g.getName());
        cmnd.setText(g.getIdCard());
        sdt.setText(g.getPhone());
        ngsinh.setDate(g.getBirthday());
        if (g.getGender() == 1) {
            jNam.setSelected(true);
        } else {
            jNu.setSelected(true);
        }
        this.username = username;
    }
    
